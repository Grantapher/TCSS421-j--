package pass;

public class Comparison {

    public static boolean lessThan(int a, int b) {
        return a < b;
    }

    public static boolean greaterThan(int a, int b) {
        return a > b;
    }

    public static boolean lessEqual(int a, int b) {
        return a <= b;
    }

    public static boolean greaterEqual(int a, int b) {
        return a >= b;
    }

}
