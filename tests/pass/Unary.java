package pass;

public class Unary {

    public static int iComp(int i) {
        return ~i;
    }

    public static long lComp(long i) {
        return ~i;
    }

    public static int preDec(int i) {
        return --i;
    }

    public static int postInc(int i) {
        return i++;
    }


}

