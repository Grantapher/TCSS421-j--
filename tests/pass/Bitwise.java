package pass;

public class Bitwise {

    public static int iOr(int i, int mask) {
        return i | mask;
    }

    public static long lOr(long i, long mask) {
        return i | mask;
    }

    public static int iXor(int i, int mask) {
        return i ^ mask;
    }

    public static long lXor(long i, long mask) {
        return i ^ mask;
    }

    public static int iAnd(int i, int mask) {
        return i & mask;
    }

    public static long lAnd(long i, long mask) {
        return i & mask;
    }

}
